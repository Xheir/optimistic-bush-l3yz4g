function solveLSHX(ref, te, tc, T1p) {
  // LSHX: sat vapor from evap → superheated at T1' by heat exchanger
  // Heat balance: h₃ − h₃' = h₁' − h₁
  //   h₃' = h₃ − (h₁' − h₁)
  // RE = h₁' − h₄  (= h₁ − h₄') using LSHX h₄
  // Wc = h₂ − h₁'  s₁'=s₂
  const evap=interpSatByT(ref,te), cond=interpSatByT(ref,tc);
  if(!evap||!cond) return {error:"Temperature out of table range."};
  if(T1p<=te) return {error:"LSHX outlet T₁' must be > T_evap."};
  const sh1=lookupSuperheat(ref,te,T1p);
  if(!sh1) return {error:`No superheated data at P=${evap.P.toFixed(1)} kPa, T=${T1p}°C.`};
  const h1=evap.hg, v1=evap.vg*1000, s1=evap.sg;
  const h1p=sh1.h, v1p=sh1.v_L, s1p=sh1.s;
  const h3=cond.hf, v3=cond.vf*1000;
  // h₃' = h₃ − (h₁' − h₁)
  const h3p=h3-(h1p-h1);
  const h4=h3p, x4=(h4-evap.hf)/evap.hfg, v4=(evap.vf+x4*(evap.vg-evap.vf))*1000, s4=evap.sf+x4*evap.sfg;
  const st2=findTforS(ref,tc,s1p);
  if(!st2) return {error:`Cannot find h₂ at s₁'=${s1p.toFixed(4)}, P_cond=${cond.P.toFixed(1)} kPa.`};
  const h2=st2.h, v2=st2.v_L, s2=st2.s, T2=st2.T;
  const RE=h1p-h4, Wc=h2-h1p, Qr=h2-h3p, COP=RE/Wc;
  return {
    evap,cond,
    states:{
      1:{T:te,P:evap.P,h:h1,v:v1,s:s1,x:1,label:"Sat. vapor — evap exit (State 1)"},
      "1p":{T:T1p,P:evap.P,h:h1p,v:v1p,s:s1p,x:">1",label:"Superheated — LSHX exit → compressor inlet (State 1')"},
      2:{T:T2,P:cond.P,h:h2,v:v2,s:s2,x:">1",label:"Superheated — compressor exit (State 2)"},
      3:{T:tc,P:cond.P,h:h3,v:v3,s:cond.sf,x:0,label:"Sat. liquid — cond exit (State 3)"},
      "3p":{T:"—",P:cond.P,h:h3p,v:v3,s:"—",x:"<0",label:`Subcooled — LSHX exit → exp. valve (State 3')`},
      4:{T:te,P:evap.P,h:h4,v:v4,s:s4,x:f4(x4),label:"Wet mixture — evap inlet (State 4)"},
    },
    perf:{RE,Wc,Qr,COP},
    equations:[
      `Heat balance: h₃ − h₃' = h₁' − h₁`,
      `h₃' = ${f3(h3)} − (${f3(h1p)} − ${f3(h1)}) = ${f3(h3p)} kJ/kg`,
      `h₄ = h₃' = ${f3(h4)} kJ/kg`,
      `RE = h₁' − h₄ = ${f3(h1p)} − ${f3(h4)} = ${f3(RE)} kJ/kg`,
      `Wc = h₂ − h₁' = ${f3(h2)} − ${f3(h1p)} = ${f3(Wc)} kJ/kg`,
      `COP = RE / Wc = ${f4(COP)}`,
    ]
  };
}

// ═══════════════════════════════════════════════════════════════════
// MASS FLOW RATE & CAPACITY CALCULATOR
// Matches Alasco's method exactly:
//   v stored in m³/kg → convert to L/kg (*1000) for display
//   ṁᵣ = V̇₁ (L/s) / v₁ (L/kg)   [no unit conversion needed in L-space]
//   ṁᵣ = V̇₁ (m³/s) / v₁ (m³/kg) [same result]
// ═══════════════════════════════════════════════════════════════════
function calcFlowRate(res, cycleType, QA_kw, V1dot_ls) {
  const states = res.states;
  const hasLSHX = cycleType==="lshx";
  const hasShIn = cycleType==="superheat_in";
  const hasShOut = cycleType==="superheat_out";

  // Volume flow always uses compressor INLET state
  const compIn = (hasShIn||hasShOut||hasLSHX) ? states["1p"] : states["1"];
  const v1_L  = compIn.v;           // already L/kg
  const v1_m3 = v1_L / 1000;        // convert to m³/kg for m³/s output
  const stateLabel = (hasShIn||hasShOut||hasLSHX) ? "1'" : "1";
  const RE = res.perf.RE;

  if (QA_kw !== null) {
    // ṁᵣ = QA / RE
    const mr = QA_kw / RE;                      // kg/s
    const V1dot_m3s = mr * v1_m3;               // m³/s
    const V1dot_Ls  = mr * v1_L;                // L/s  = ṁᵣ (kg/s) × v₁ (L/kg)
    const Wc_kw = mr * res.perf.Wc;
    return {
      mr, v1_m3, v1_L, stateLabel,
      V1dot_Ls, V1dot_m3s,
      Wc_kw,
      stepsMr: `ṁᵣ = QA / RE = ${QA_kw.toFixed(3)} / ${RE.toFixed(3)} = ${mr.toFixed(4)} kg/s`,
      stepsV:  `V̇₁ = ṁᵣ × v${stateLabel} = ${mr.toFixed(4)} × ${v1_L.toFixed(4)} L/kg = ${V1dot_Ls.toFixed(4)} L/s`,
      stepsW:  `Ẇc = ṁᵣ × wc = ${mr.toFixed(4)} × ${res.perf.Wc.toFixed(3)} = ${Wc_kw.toFixed(3)} kW`,
    };
  }

  if (V1dot_ls !== null) {
    // ṁᵣ = V̇₁ (L/s) / v₁ (L/kg)  ← exactly Alasco's formula
    const mr = V1dot_ls / v1_L;                 // kg/s
    const QA = mr * RE;                          // kW
    const Wc_kw = mr * res.perf.Wc;
    return {
      mr, v1_m3, v1_L, stateLabel,
      V1dot_Ls: V1dot_ls,
      QA_kw: QA, QA_TR: QA / TR_TO_KW,
      Wc_kw,
      stepsMr: `v${stateLabel} = ${v1_L.toFixed(4)} L/kg   →   ṁᵣ = V̇₁ / v${stateLabel} = ${V1dot_ls} / ${v1_L.toFixed(4)} = ${mr.toFixed(4)} kg/s`,
      stepsQA: `QA = ṁᵣ × RE = ${mr.toFixed(4)} × ${RE.toFixed(3)} = ${QA.toFixed(3)} kW = ${(QA/TR_TO_KW).toFixed(4)} TR`,
      stepsW:  `Ẇc = ṁᵣ × wc = ${mr.toFixed(4)} × ${res.perf.Wc.toFixed(3)} = ${Wc_kw.toFixed(3)} kW`,
    };
  }
  return null;
}

