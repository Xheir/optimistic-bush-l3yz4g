// INTERPOLATION HELPERS
// ═══════════════════════════════════════════════════════════════════
function interpSatByT(ref, T) {
  const tbl = SAT[ref];
  if (T < tbl[0].t || T > tbl[tbl.length-1].t) return null;
  let i = tbl.findIndex(r => r.t >= T);
  if (i === 0) return {...tbl[0]};
  const r0=tbl[i-1], r1=tbl[i];
  if (r0.t===T) return {...r0};
  const f=(T-r0.t)/(r1.t-r0.t);
  const res={t:T};
  for (const k of ["P","hf","hg","hfg","sf","sg","sfg","vf","vg"])
    res[k]=r0[k]+f*(r1[k]-r0[k]);
  return res;
}

function interpSatByP(ref, P) {
  const tbl = SAT[ref];
  if (P < tbl[0].P || P > tbl[tbl.length-1].P) return null;
  let i = tbl.findIndex(r => r.P >= P);
  if (i === 0) return {...tbl[0]};
  const r0=tbl[i-1], r1=tbl[i];
  if (r0.P===P) return {...r0};
  const f=(P-r0.P)/(r1.P-r0.P);
  const res={P};
  for (const k of ["t","hf","hg","hfg","sf","sg","sfg","vf","vg"])
    res[k]=r0[k]+f*(r1[k]-r0[k]);
  return res;
}

// ── Get rows for a Tsat block, interpolating between two blocks if needed
// Returns [[T_actual, v_L/kg, h, s], ...]
function getSuperRows(ref, Tsat) {
  const tbl = SUPERHEAT[ref]; if (!tbl) return null;
  const keys = Object.keys(tbl).map(Number).sort((a,b)=>a-b);
  // Clamp to table range
  if (Tsat <= keys[0]) return tbl[String(keys[0])];
  if (Tsat >= keys[keys.length-1]) return tbl[String(keys[keys.length-1])];
  // Find bracket
  let ki = keys.findIndex(k => k >= Tsat);
  const T0=keys[ki-1], T1=keys[ki];
  if (Tsat === T0) return tbl[String(T0)];
  if (Tsat === T1) return tbl[String(T1)];
  const ft = (Tsat-T0)/(T1-T0);
  const rows0 = tbl[String(T0)], rows1 = tbl[String(T1)];
  // Merge T_actual lists
  const Tacts = [...new Set([...rows0.map(r=>r[0]),...rows1.map(r=>r[0])])].sort((a,b)=>a-b);
  function atT(rows, Ta) {
    if (Ta < rows[0][0] || Ta > rows[rows.length-1][0]) return null;
    let i = rows.findIndex(r=>r[0]>=Ta);
    if (i===0) return rows[0].slice(1);
    const r0=rows[i-1],r1=rows[i],f=(Ta-r0[0])/(r1[0]-r0[0]);
    return [r0[1]+f*(r1[1]-r0[1]), r0[2]+f*(r1[2]-r0[2]), r0[3]+f*(r1[3]-r0[3])];
  }
  return Tacts.map(Ta => {
    const a=atT(rows0,Ta), b=atT(rows1,Ta);
    if (!a && !b) return null;
    const vals = a&&b ? [0,1,2].map(i=>a[i]+ft*(b[i]-a[i])) : (a||b);
    return [Ta, ...vals];
  }).filter(Boolean);
}

// ── Lookup superheated props at a given Tsat_evap and T_actual
// Returns {v_L: L/kg, h, s} — v_L is in L/kg matching Stoecker A-7
function lookupSuperheat(ref, Tsat, T_actual) {
  const rows = getSuperRows(ref, Tsat); if (!rows) return null;
  const lastRow = rows[rows.length-1];
  const firstRow = rows[0];
  if (T_actual < firstRow[0]) return null;
  if (T_actual > lastRow[0] + 30) return null;
  if (T_actual > lastRow[0]) {
    const r0=rows[rows.length-2], r1=rows[rows.length-1];
    const ft=(T_actual-r0[0])/(r1[0]-r0[0]);
    return {v_L:r0[1]+ft*(r1[1]-r0[1]), h:r0[2]+ft*(r1[2]-r0[2]), s:r0[3]+ft*(r1[3]-r0[3])};
  }
  let i = rows.findIndex(r=>r[0]>=T_actual);
  if (i===0) return {v_L:rows[0][1], h:rows[0][2], s:rows[0][3]};
  const r0=rows[i-1],r1=rows[i],ft=(T_actual-r0[0])/(r1[0]-r0[0]);
  return {v_L:r0[1]+ft*(r1[1]-r0[1]), h:r0[2]+ft*(r1[2]-r0[2]), s:r0[3]+ft*(r1[3]-r0[3])};
}

// ── Find T_actual where s = s_target at given Tsat (for isentropic compression)
// Returns {T, v_L: L/kg, h, s}
function findTforS(ref, Tsat, s_target) {
  const rows = getSuperRows(ref, Tsat); if (!rows) return null;
  const lastRow = rows[rows.length-1];
  if (s_target > lastRow[3] + 0.05) return null;
  if (s_target > lastRow[3]) {
    const r0=rows[rows.length-2], r1=rows[rows.length-1];
    const ft=(s_target-r0[3])/(r1[3]-r0[3]);
    return {T:r0[0]+ft*(r1[0]-r0[0]), v_L:r0[1]+ft*(r1[1]-r0[1]), h:r0[2]+ft*(r1[2]-r0[2]), s:s_target};
  }
  let i = rows.findIndex(r=>r[3]>=s_target);
  if (i<0) return null;
  if (i===0) return {T:rows[0][0], v_L:rows[0][1], h:rows[0][2], s:rows[0][3]};
  const r0=rows[i-1],r1=rows[i];
  const ft=(s_target-r0[3])/(r1[3]-r0[3]);
  return {T:r0[0]+ft*(r1[0]-r0[0]), v_L:r0[1]+ft*(r1[1]-r0[1]), h:r0[2]+ft*(r1[2]-r0[2]), s:s_target};
}

